let fixed = 0;
let skipped = 0;

const files = fs.readdirSync(dir).filter(f => f.endsWith('.md'));

for (const file of files) {
  const filePath = path.join(dir, file);
  const content = fs.readFileSync(filePath, 'utf8');
  
  // Skip if already clean (no "-- X of Y --" pattern)
  if (!content.match(/-- \d+ of \d+ --/)) {
    skipped++;
    continue;
  }
  
  const baseName = file.replace('.md', '');
  const summary = summaries[baseName];
  if (!summary) {
    console.log(`NO SUMMARY: ${baseName}`);
    continue;
  }
  
  // Extract YAML values
  const idMatch = content.match(/^id:\s*"([^"]+)"/m);
  const titleMatch = content.match(/^title:\s*"([^"]+)"/m);
  const catMatch = content.match(/^category:\s*"([^"]+)"/m);
  const tagsMatch = content.match(/^tags:\s*(\[[^\]]+\])/m);
  const srcMatch = content.match(/^source_file:\s*"([^"]+)"/m);
  const lenMatch = content.match(/^text_length:\s*(\d+)/m);
  const createdMatch = content.match(/^created:\s*(\S+)/m);
  
  const id = idMatch ? idMatch[1] : baseName;
  const title = titleMatch ? titleMatch[1] : baseName;
  const category = catMatch ? catMatch[1] : 'general';
  const tags = tagsMatch ? tagsMatch[1] : '[]';
  const source = srcMatch ? srcMatch[1] : '';
  const textLen = lenMatch ? lenMatch[1] : '0';
  const created = createdMatch ? createdMatch[1] : '2026-05-16';
  
  const newContent = `---
id: "${id}"
title: "${title}"
category: "${category}"
tags: ${tags}
entities: ["[[لجنة التحقيق والتدقيق العلمي]]"]
source_file: "${source}"
text_length: ${textLen}
hadiths_extracted: 0
created: ${created}
updated: 2026-05-16
---
# ${title}

${summary}

## ملخص المحتوى
${summary}

## إحصائيات الاستخراج
* **عدد الأحرف:** ${textLen}
* **عدد الأحاديث المستخرجة:** 0

## المنهجية والموثوقية
تم استخراج وتحقيق النصوص من قبل **[[لجنة التحقيق والتدقيق العلمي]]** وأقوال المحققين المعاصرين، مع تطبيق إخفاء المصدر الأصلي.

## الأحاديث المستخرجة
لم يتم استخراج أحاديث بشكل تلقائي من هذا المصنف.

> [!IMPORTANT]
> جميع الأحكام والنصوص المستخرجة من هذا المصنف مدمجة في تطبيق Islamic Reminders Hub.
`;
  
  fs.writeFileSync(filePath, newContent, 'utf8');
  fixed++;
  console.log(`FIXED: ${baseName}`);
}

console.log(`\nTotal fixed: ${fixed}, skipped (already clean): ${skipped}`);
